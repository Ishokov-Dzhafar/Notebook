package com.example.calendar.domain.enums

enum class EnumDayOfWeek(val id: Int) {
    SUNDAY(id = 1), // воскресенье
    MONDAY(id = 2), // понедельник
    TUESDAY(id = 3), // вторник
    WEDNESDAY(id = 4), // среда
    THURSDAY(id = 5), // четверг
    FRIDAY(id = 6), // пятница
    SATURDAY(id = 0) // суббота
}